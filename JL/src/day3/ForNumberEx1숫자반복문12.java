package day3;

public class ForNumberEx1숫자반복문12 {

	public static void main(String[] args) {
		/* 1부터 5까지 출력하는 코드를 for문으로 작성하세요.
		 * 반복횟수 : 5번. 1부터 5까지(같다를 포함) 1씩 증가
		 * 출력예시
		 * 1
		 * 2
		 * 3
		 * 4
		 * 5
		 * */
		int num;
		for(num = 1 ; num <= 5 ; num++ ) {
		System.out.println(num);
		}
	}

}
